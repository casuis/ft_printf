char		*ft_create_table()
{
	char	*ret;

	if (!(ret = malloc(sizeof(char) * 9)))
		return (NULL);
	ret[0] = 'c';
	ret[1] = 'x';
	ret[2] = 'X';
	ret[3] = 'u';
	ret[4] = 'p';
	ret[5] = 'd';
	ret[6] = 'i';
	ret[7] = 's';
	ret[8] = '\0';
	return (ret);
}


